let modalText = document.getElementById("modalText");
let todoContainer = document.getElementById("todoContainer");
let progressContainer = document.getElementById("todoProgress");
let doneContainer = document.getElementById("todoContainer3");
let blockContainer = document.getElementById("blockContainer");

let tasks = [];

let todoInput = document.getElementsByClassName("todo-input")[0];
let todoSelect = document.getElementById("statusSelect");
let todoSubmitButton = document.getElementById("submitButton");

function submitTask() {
  let taskText = todoInput.value;
  let todoStatus = todoSelect.value;

  let task = {
    text: taskText,
    status: todoStatus,
  };

  tasks.push(task);
  renderTasks();
}

todoSubmitButton.onclick = submitTask;

function renderTasks() {
  let todoTasksHTML = "";
  let inprogressTasksHTML = "";

  tasks.forEach((task) => {
    let _task = createTaskElement(task.text);
    if (task.status === "todo") {
      todoTasksHTML = todoTasksHTML + _task;
    }
    if (task.status === "inprogress") {
      inprogressTasksHTML = inprogressTasksHTML + _task;
    }
  });
  todoContainer.innerHTML = todoTasksHTML;
  progressContainer.innerHTML = inprogressTasksHTML;
}

function createTaskElement(text) {
  let _task = `<div class="todo-section2">
  <div id="Dugui4Container"></div>
  <div class="todo-comp">
    <p>${text}</p>
  </div>
  <div class="zurag">
    <img src="./Vector (1).png" />
    <img src="./Frame.png" />
  </div>
</div>`;
  return _task;
}
